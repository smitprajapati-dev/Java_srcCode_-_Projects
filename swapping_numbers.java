public class swapping_numbers {
    public static void main(String [] args){

        int a =12;
        int b = 91;
        int temp;

        temp = a;
        a = b;
        b  = temp;
        System.out.println("The value of a : " +  a);
        System.out.println("The value of b :" +  b);
    }
    
}
